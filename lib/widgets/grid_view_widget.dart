

// grid_view_widget

// このwidgetは、複数のreels動画をグリッドビューで表示するための使い回すwidgetです。

// 横方向に二つのreels動画を配置する。
// グリッドビューの表示するデータは、どこのページのこのページかで変わる。
// 投稿履歴、いいね履歴、購入履歴、search_pageでグリッドビューの時、postデータのうちの動画のみを表示しており、常に音無しで再生されている。
// cart_pageでグリッドビューの時、postデータのうちの動画と価格と商品の個数可変のためのcounterだけが表示されている。
// これら全てのグリッドビューのページでグリッドをタップした時、reels動画のページに遷移する。

// 検索をするときは、全ての投稿データから検索できないといけない。